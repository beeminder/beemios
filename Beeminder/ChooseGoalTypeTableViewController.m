- (void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender
{
    NSIndexPath *path = [self.tableView indexPathForSelectedRow];
    
    NSString *goalType = [self.goalTypes objectAtIndex:path.row];
    
    [segue.destinationViewController performSelector:@selector(setGoalType:) withObject:(goalType)];
