#import "UIFont+BeeminderFonts.h"

@implementation UIFont (BeeminderFonts)

+ (UIFont *)defaultFont
{
    return [UIFont fontWithName:@"Lato" size:15.0f];
}

+ (UIFont *)defaultFontBold
{
    return [UIFont fontWithName:@"Lato-Bold" size:15.0f];
}


@end
